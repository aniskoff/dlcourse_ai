import numpy as np


def softmax(predictions):
    '''
    Computes probabilities from scores

    Arguments:
      predictions, np array, shape is either (N) or (batch_size, N) -
        classifier output

    Returns:
      probs, np array of the same shape as predictions -
        probability for every class, 0..1
    '''
    # TODO implement softmax

    predictions_c = predictions.copy()

    if predictions_c.ndim == 1:
        predictions_c -= np.max(predictions_c)
        res = np.exp(predictions_c) / np.sum(np.exp(predictions_c))
        return res

    predictions_c -= np.max(predictions_c, axis=1, keepdims=True)
    exps = np.exp(predictions_c)
    res = exps / np.sum(exps, axis=1, keepdims=True)
    return res


def cross_entropy_loss(probs, target_index):
    '''
    Computes cross-entropy loss

    Arguments:
      probs, np array, shape is either (N) or (batch_size, N) -
        probabilities for every class
      target_index: np array of int, shape is (1) or (batch_size) -
        index of the true class for given sample(s)

    Returns:
      loss: single value
    '''
    # TODO implement cross-entropy
    if probs.ndim == 1:
        loss_i = -np.log(probs[target_index])
        return loss_i

    loss = - np.mean(np.log(probs[np.arange(probs.shape[0]), target_index.T]))
    return loss


def l2_regularization(W, reg_strength):
    """
    Computes L2 regularization loss on weights and its gradient

    Arguments:
      W, np array - weights
      reg_strength - float value

    Returns:
      loss, single value - l2 regularization loss
      gradient, np.array same shape as W - gradient of weight by l2 loss
    """
    # TODO: Copy from the previous assignment
    loss = reg_strength * np.sum(W ** 2)
    dR = 2 * reg_strength * W

    return loss, dR


def softmax_with_cross_entropy(preds, target_index):
    """
    Computes softmax and cross-entropy loss for model predictions,
    including the gradient

    Arguments:
      predictions, np array, shape is either (N) or (batch_size, N) -
        classifier output
      target_index: np array of int, shape is (1) or (batch_size) -
        index of the true class for given sample(s)

    Returns:
      loss, single value - cross-entropy loss
      dprediction, np array same shape as predictions - gradient of predictions by loss value
    """
    # TODO: Copy from the previous assignment
    probs = softmax(preds)
    loss = cross_entropy_loss(probs, target_index)

    if probs.ndim == 1:
        d_preds = probs
        d_preds[target_index] -= 1

    else:
        m = probs.shape[0]
        tmp = np.zeros(probs.shape)
        tmp[np.arange(probs.shape[0]), target_index.T] = 1
        d_preds = (probs - tmp) / m

    return loss, d_preds


